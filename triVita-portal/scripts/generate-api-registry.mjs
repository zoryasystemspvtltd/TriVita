/**
 * Scans HealthcarePlatform *Controller.cs files and emits a typed registry of API resources
 * for the portal (standard list/get/create/update/delete + detected auxiliary POST routes).
 *
 * Run from repo root: node triVita-portal/scripts/generate-api-registry.mjs
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, '..', '..');
const HEALTHCARE = path.join(REPO_ROOT, 'HealthcarePlatform');
const OUT_FILE = path.join(__dirname, '..', 'src', 'generated', 'apiRegistry.ts');

function walkControllers(dir, acc = []) {
  if (!fs.existsSync(dir)) return acc;
  for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, ent.name);
    if (ent.isDirectory()) {
      if (ent.name === 'bin' || ent.name === 'obj' || ent.name === 'node_modules') continue;
      walkControllers(p, acc);
    } else if (ent.isFile() && ent.name.endsWith('Controller.cs')) {
      acc.push(p);
    }
  }
  return acc;
}

function inferModule(absPath) {
  const s = absPath.replace(/\\/g, '/');
  if (s.includes('/HMSService/')) return 'hms';
  if (s.includes('/LISService/')) return 'lis';
  if (s.includes('/LMSService/')) return 'lms';
  if (s.includes('/PharmacyService/')) return 'pharmacy';
  if (s.includes('/SharedService/')) return 'shared';
  if (s.includes('/CommunicationService/')) return 'communication';
  if (s.includes('/IdentityService/')) return 'identity';
  return null;
}

function titleFromPath(routePath) {
  return routePath
    .split('/')
    .map((seg) =>
      seg
        .split('-')
        .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
        .join(' ')
    )
    .join(' › ');
}

function analyzeChunk(chunk) {
  const hasPagedList = /PagedQuery/.test(chunk) && /\[HttpGet\]\s*\r?\n/.test(chunk);
  const hasGetById =
    /\[HttpGet\("\{id:long\}"\)\]/.test(chunk) ||
    /\[HttpGet\("\{id:int\}"\)\]/.test(chunk) ||
    /\[HttpGet\("\{[^}]*Id:long\}"\)\]/.test(chunk) ||
    /\[HttpGet\("\{[^}]*Id:int\}"\)\]/.test(chunk);
  const hasCreate = /\[HttpPost\]\s*\r?\n/.test(chunk);
  const hasUpdate = /\[HttpPut\("\{id:long\}"\)\]/.test(chunk) || /\[HttpPut\("\{id:int\}"\)\]/.test(chunk);
  const hasDelete = /\[HttpDelete\("\{id:long\}"\)\]/.test(chunk) || /\[HttpDelete\("\{id:int\}"\)\]/.test(chunk);

  const auxiliaryPosts = [];
  const postAttr = [...chunk.matchAll(/\[HttpPost\("([^"]+)"\)\]/g)];
  for (const m of postAttr) {
    const rel = m[1];
    if (rel === '{id:long}' || rel === '{id:int}') continue;
    if (/^\{id:long\}\//.test(rel) || /^\{id:int\}\//.test(rel)) {
      auxiliaryPosts.push({
        relPath: rel.replace(/^\{id:(?:long|int)\}\//, '{id}/'),
        label: titleFromPath(rel.replace(/^\{id:(?:long|int)\}\//, '')),
        usesId: true,
      });
      continue;
    }
    if (!rel.includes('{')) {
      auxiliaryPosts.push({ relPath: rel, label: titleFromPath(rel), usesId: false });
    }
  }

  /** GET with static subpath (non–by-id), e.g. integration/query-test */
  const auxiliaryGets = [];
  for (const m of chunk.matchAll(/\[HttpGet\("([^"]+)"\)\]/g)) {
    const rel = m[1];
    if (rel.includes('{')) continue;
    auxiliaryGets.push({ relPath: rel, label: titleFromPath(rel) });
  }

  return {
    hasPagedList,
    hasGetById,
    hasCreate,
    hasUpdate,
    hasDelete,
    auxiliaryPosts,
    auxiliaryGets,
  };
}

function parseControllerFile(filePath) {
  const text = fs.readFileSync(filePath, 'utf8');
  const module = inferModule(filePath);
  if (!module) return [];

  const parts = text.split(/(?=\[Route\("api\/v\{version:apiVersion\}\/)/);
  const out = [];

  for (const part of parts) {
    const rm = part.match(/^\[Route\("api\/v\{version:apiVersion\}\/([^"]+)"\)\]/);
    if (!rm) continue;
    const routePath = rm[1];
    if (routePath.startsWith('auth')) continue;

    const caps = analyzeChunk(part);

    out.push({
      module,
      path: routePath,
      title: titleFromPath(routePath),
      ...caps,
    });
  }
  return out;
}

function dedupe(entries) {
  const map = new Map();
  for (const e of entries) {
    const k = `${e.module}:${e.path}`;
    if (!map.has(k)) map.set(k, e);
  }
  return [...map.values()].sort((a, b) => (a.module + a.path).localeCompare(b.module + b.path));
}

function main() {
  const controllers = walkControllers(HEALTHCARE);
  const raw = [];
  for (const f of controllers) {
    raw.push(...parseControllerFile(f));
  }
  const entries = dedupe(raw);

  const lines = [];
  lines.push(`/* eslint-disable */`);
  lines.push(`/** Auto-generated by scripts/generate-api-registry.mjs — do not edit by hand. */`);
  lines.push(``);
  lines.push(`export type ApiRegistryModule = 'hms' | 'lis' | 'lms' | 'pharmacy' | 'shared' | 'communication' | 'identity';`);
  lines.push(``);
  lines.push(`export interface ApiRegistryAuxiliaryPost {`);
  lines.push(`  relPath: string;`);
  lines.push(`  label: string;`);
  lines.push(`  usesId: boolean;`);
  lines.push(`}`);
  lines.push(``);
  lines.push(`export interface ApiRegistryAuxiliaryGet {`);
  lines.push(`  relPath: string;`);
  lines.push(`  label: string;`);
  lines.push(`}`);
  lines.push(``);
  lines.push(`export interface ApiRegistryEntry {`);
  lines.push(`  module: ApiRegistryModule;`);
  lines.push(`  /** Path after /api/v1/ (may contain slashes). */`);
  lines.push(`  path: string;`);
  lines.push(`  title: string;`);
  lines.push(`  hasPagedList: boolean;`);
  lines.push(`  hasGetById: boolean;`);
  lines.push(`  hasCreate: boolean;`);
  lines.push(`  hasUpdate: boolean;`);
  lines.push(`  hasDelete: boolean;`);
  lines.push(`  auxiliaryPosts: readonly ApiRegistryAuxiliaryPost[];`);
  lines.push(`  auxiliaryGets: readonly ApiRegistryAuxiliaryGet[];`);
  lines.push(`}`);
  lines.push(``);
  lines.push(`export const generatedApiRegistry: readonly ApiRegistryEntry[] = [`);
  for (const e of entries) {
    const ap = JSON.stringify(e.auxiliaryPosts);
    const ag = JSON.stringify(e.auxiliaryGets);
    lines.push(
      `  { module: '${e.module}', path: ${JSON.stringify(e.path)}, title: ${JSON.stringify(e.title)}, hasPagedList: ${e.hasPagedList}, hasGetById: ${e.hasGetById}, hasCreate: ${e.hasCreate}, hasUpdate: ${e.hasUpdate}, hasDelete: ${e.hasDelete}, auxiliaryPosts: ${ap} as const, auxiliaryGets: ${ag} as const },`
    );
  }
  lines.push(`] as const;`);
  lines.push(``);

  fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
  fs.writeFileSync(OUT_FILE, lines.join('\n') + '\n', 'utf8');
  console.log(`Wrote ${entries.length} entries to ${path.relative(REPO_ROOT, OUT_FILE)}`);
}

main();
